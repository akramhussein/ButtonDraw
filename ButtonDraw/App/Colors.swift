import UIKit

extension UIColor {
    public class var primaryColor: UIColor {
        return UIColor(rgba: "#FF0000")
    }

    public class var unselectedButtonBackgroundColor: UIColor {
        return UIColor(rgba: "#FF715B")
    }

    public class var green: UIColor {
        return UIColor(rgba: "#0E7C7B")
    }

    public class var pink: UIColor {
        return UIColor(rgba: "#EC0868")
    }

    public class var red: UIColor {
        return UIColor(rgba: "#FF0000")
    }

    public class var blue: UIColor {
        return UIColor(rgba: "#4472CA")
    }
    
    public class var coral: UIColor {
        return UIColor(rgba: "#FF715B")
    }
}
